#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*s1;

	i = 0;
	s1 = (char *)s;
	while (s1[i] != c)
	{
		if (s1[i] == '\0')
			return (NULL);
		else
			i++;
	}
	return (&s1[i]);
}
